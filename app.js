let state = loadState();

function loadState() {
  const fallback = {
    currentLessonId: 1,
    completed: [],
    mistakes: [],
    activeFraction: "1/2",
    printWeek: 1,
    mentalQuestions: [],
    mentalChecked: false,
    overlapRevealed: false,
  };

  try {
    const saved = JSON.parse(localStorage.getItem(storageKey));
    return { ...fallback, ...saved };
  } catch (error) {
    return fallback;
  }
}

function saveState() {
  try {
    localStorage.setItem(storageKey, JSON.stringify(state));
  } catch (error) {
    // The page still works if localStorage is unavailable.
  }
}

function $(selector, root = document) {
  return root.querySelector(selector);
}

function esc(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function abilityById(id) {
  return abilities.find((ability) => ability.id === id);
}

function lessonById(id) {
  return lessons.find((lesson) => lesson.id === Number(id)) || lessons[0];
}

function isDone(id) {
  return state.completed.includes(Number(id));
}

function setDone(id, done) {
  const lessonId = Number(id);
  if (done && !isDone(lessonId)) {
    state.completed.push(lessonId);
  }
  if (!done) {
    state.completed = state.completed.filter((doneId) => doneId !== lessonId);
  }
  state.completed.sort((a, b) => a - b);
  saveState();
}

