function continueNextLesson() {
  const next = lessons.find((lesson) => !isDone(lesson.id)) || lessons[lessons.length - 1];
  goToLesson(next.id);
}

function randomInt(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function makeMentalQuestion(mode) {
  const type = mode === "mixed" ? ["add", "sub", "mul", "div", "two"][randomInt(0, 4)] : mode;

  if (type === "easy") {
    const a = randomInt(2, 40);
    const b = randomInt(2, 30);
    return { text: `${a} + ${b}`, answer: a + b };
  }

  if (type === "addsub") {
    const add = Math.random() > 0.5;
    const a = randomInt(20, 180);
    const b = randomInt(10, 90);
    return add ? { text: `${a} + ${b}`, answer: a + b } : { text: `${a + b} - ${b}`, answer: a };
  }

  if (type === "muldiv") {
    const a = randomInt(2, 9);
    const b = randomInt(2, 9);
    return Math.random() > 0.5 ? { text: `${a} × ${b}`, answer: a * b } : { text: `${a * b} ÷ ${a}`, answer: b };
  }

  if (type === "two") {
    const a = randomInt(2, 9);
    const b = randomInt(2, 9);
    const c = randomInt(5, 30);
    return Math.random() > 0.5 ? { text: `${a} × ${b} + ${c}`, answer: a * b + c } : { text: `${c + a * b} - ${a} × ${b}`, answer: c };
  }

  if (type === "sub") {
    const a = randomInt(40, 160);
    const b = randomInt(10, 60);
    return { text: `${a} - ${b}`, answer: a - b };
  }

  const a = randomInt(20, 150);
  const b = randomInt(10, 80);
  return { text: `${a} + ${b}`, answer: a + b };
}

function generateMentalPractice() {
  const mode = $("#mentalMode").value;
  state.mentalQuestions = Array.from({ length: 12 }, () => makeMentalQuestion(mode));
  state.mentalChecked = false;
  saveState();
  renderMentalPractice();
}

function renderMentalPractice() {
  if (!state.mentalQuestions.length) {
    state.mentalQuestions = Array.from({ length: 12 }, () => makeMentalQuestion("mixed"));
  }

  $("#mentalBoard").innerHTML = `
    ${state.mentalQuestions
      .map(
        (question, index) => `
          <div class="mental-item" data-mental-index="${index}">
            <strong>${index + 1}. ${esc(question.text)} =</strong>
            <input type="number" inputmode="numeric" aria-label="第${index + 1}题答案" />
          </div>
        `,
      )
      .join("")}
  `;
}

function checkMentalAnswers() {
  let correct = 0;
  document.querySelectorAll(".mental-item").forEach((item) => {
    const index = Number(item.dataset.mentalIndex);
    const input = $("input", item);
    const value = Number(input.value);
    const answered = input.value.trim() !== "";
    const right = answered && value === state.mentalQuestions[index].answer;
    item.classList.toggle("correct", right);
    item.classList.toggle("wrong", answered && !right);
    if (right) correct += 1;
  });

  $(".mental-result")?.remove();
  $("#mentalBoard").insertAdjacentHTML(
    "beforeend",
    `<div class="mental-result">本次答对 ${correct}/12。错题建议记录到错题本，并写清错因。</div>`,
  );
}

