function bindEvents() {
  document.querySelectorAll(".nav-button[data-view]").forEach((button) => {
    button.addEventListener("click", () => showView(button.dataset.view));
  });

  $("#continueButton").addEventListener("click", continueNextLesson);

  $("#weekOverview").addEventListener("click", (event) => {
    const card = event.target.closest("[data-lesson-id]");
    if (card) goToLesson(card.dataset.lessonId);
  });

  $("#lessonSelect").addEventListener("change", (event) => goToLesson(event.target.value));

  $("#lessonDetail").addEventListener("change", (event) => {
    if (event.target.id === "completeLesson") {
      setDone(state.currentLessonId, event.target.checked);
      renderOverview();
      renderLesson();
    }
  });

  $("#lessonDetail").addEventListener("click", (event) => {
    const button = event.target.closest("[data-lesson-nav]");
    if (!button) return;
    const offset = button.dataset.lessonNav === "next" ? 1 : -1;
    goToLesson(state.currentLessonId + offset);
  });

  $("#generateMental").addEventListener("click", generateMentalPractice);
  $("#checkMental").addEventListener("click", checkMentalAnswers);

  $("#fractionButtons").addEventListener("click", (event) => {
    const button = event.target.closest("[data-fraction]");
    if (!button) return;
    state.activeFraction = button.dataset.fraction;
    saveState();
    renderFractionControls();
    renderFractionLab();
  });

  $("#checkOverlap").addEventListener("click", checkOverlap);

  $("#mistakeForm").addEventListener("submit", addMistake);
  $("#mistakeList").addEventListener("click", (event) => {
    const button = event.target.closest("[data-delete-mistake]");
    if (button) deleteMistake(button.dataset.deleteMistake);
  });

  $("#printWeek").addEventListener("change", (event) => {
    state.printWeek = Number(event.target.value);
    saveState();
    renderPrintSheet();
  });
  $("#printButton").addEventListener("click", () => window.print());
}

renderAll();
