function overlapAnswer() {
  return overlapCells.a.filter((cell) => overlapCells.b.includes(cell)).length;
}

function renderOverlapPractice(feedback = "输入你数到的重叠格子数，再点击检查。") {
  const both = overlapCells.a.filter((cell) => overlapCells.b.includes(cell));
  const total = Array.from({ length: 36 }, (_, index) => index + 1);
  $("#overlapBoard").innerHTML = `
    <div class="overlap-grid">
      ${total
        .map((cell) => {
          const inA = overlapCells.a.includes(cell);
          const inB = overlapCells.b.includes(cell);
          const className = inA && inB ? "both" : inA ? "a" : inB ? "b" : "";
          const reveal = state.overlapRevealed && both.includes(cell) ? "reveal" : "";
          return `<span class="overlap-cell ${className} ${reveal}" aria-label="第${cell}格"></span>`;
        })
        .join("")}
    </div>
    <div class="overlap-side">
      <div class="legend">
        <span><i class="blue"></i>蓝色区域：${overlapCells.a.length}格</span>
        <span><i class="green"></i>绿色区域：${overlapCells.b.length}格</span>
        <span><i class="both"></i>同时属于两边</span>
      </div>
      <div class="overlap-feedback">${esc(feedback)}</div>
      <p>提示：可以先数蓝色，再数绿色，最后专门圈出同时有两种颜色的格子。</p>
    </div>
  `;
}

function checkOverlap() {
  const answer = Number($("#overlapAnswer").value);
  const correct = overlapAnswer();
  state.overlapRevealed = true;
  saveState();
  const feedback =
    answer === correct
      ? `答对了。重叠格子是 ${correct} 个。`
      : `还需要复查。正确重叠格子是 ${correct} 个，蓝绿交叉处已高亮。`;
  renderOverlapPractice(feedback);
}

function renderMistakes() {
  if (!state.mistakes.length) {
    $("#mistakeList").innerHTML = '<div class="empty-state">还没有错题记录。完成练习后，把最典型的1-2个问题记在这里。</div>';
    return;
  }

  $("#mistakeList").innerHTML = state.mistakes
    .map((mistake) => {
      const lesson = lessonById(mistake.lessonId);
      return `
        <article class="mistake-row">
          <p><strong>第${lesson.number}课</strong><small>${esc(lesson.title)} · ${esc(mistake.date)}</small></p>
          <p><span class="tag">${esc(mistake.type)}</span></p>
          <p>${esc(mistake.text)}</p>
          <button class="danger-button" type="button" data-delete-mistake="${mistake.id}">删除</button>
        </article>
      `;
    })
    .join("");
}

function addMistake(event) {
  event.preventDefault();
  const text = $("#mistakeText").value.trim();
  if (!text) return;

  state.mistakes.unshift({
    id: String(Date.now()),
    lessonId: Number($("#mistakeLesson").value),
    type: $("#mistakeType").value,
    text,
    date: new Date().toLocaleDateString("zh-CN"),
  });
  $("#mistakeText").value = "";
  saveState();
  renderMistakes();
}

function deleteMistake(id) {
  state.mistakes = state.mistakes.filter((mistake) => mistake.id !== id);
  saveState();
  renderMistakes();
}

